export function getNextHomeworkSuggestion(studentProgress) {
  const surahId = studentProgress?.lastMemorizedSurahId || 1;
  const lastVerse = studentProgress?.lastMemorizedVerse || 0;
  
  // Find current surah details
  const surah = quranSurahs.find(s => s.id === surahId) || quranSurahs[0];
  
  let nextSurahId = surahId;
  let startVerse = lastVerse + 1;
  let endVerse = startVerse + 15; // Propose 15 verses (safe and standard homework size)
  
  if (startVerse > surah.verses) {
    // Propose next surah
    nextSurahId = surahId < 114 ? surahId + 1 : 1;
    const nextSurah = quranSurahs.find(s => s.id === nextSurahId) || quranSurahs[0];
    startVerse = 1;
    endVerse = Math.min(15, nextSurah.verses);
  } else {
    endVerse = Math.min(endVerse, surah.verses);
  }
  
  const targetSurah = quranSurahs.find(s => s.id === nextSurahId);
  return {
    surahId: nextSurahId,
    surahName: targetSurah.name,
    startVerse,
    endVerse,
    text: `حفظ سورة ${targetSurah.name} من الآية ${startVerse} إلى ${endVerse}`
  };
}

